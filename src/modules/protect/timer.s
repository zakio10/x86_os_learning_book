;************************************************************************
;	タイマーの設定
;------------------------------------------------------------------------
;	タイマー（カウンタ0）割り込み許可
;========================================================================
;■書式		: void int_en_timer0(void);
;
;■引数		: 無し
;
;■戻り値	: 無し
;************************************************************************
int_en_timer0:
		;---------------------------------------
		; 【レジスタの保存】
		;---------------------------------------
		push	eax

		;---------------------------------------
		;	8254 Timer
		;	0x2e9c(11932)=10[ms] @ CLK=1,193,182[Hz]
		;---------------------------------------
		 outp	 0x43, 0b_00_11_010_0			; // カウンタ0, 下位/上位で書き込み, モード2, バイナリ
		 outp	 0x40, 0x9C						; // 下位バイト
		 outp	 0x40, 0x2E						; // 上位バイト

		;---------------------------------------
		; 【レジスタの復帰】
		;---------------------------------------
		pop		eax

		ret

